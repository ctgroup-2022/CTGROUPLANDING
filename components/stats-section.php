<script>
document.addEventListener('DOMContentLoaded', function() {
    // Counter Animation
    const counters = document.querySelectorAll('.ct-counter');
    let hasAnimated = false;
    
    const animateCounters = () => {
        if (hasAnimated) return;
        hasAnimated = true;
        
        counters.forEach((counter, index) => {
            const target = parseInt(counter.getAttribute('data-count'));
            const duration = 2000; // 2 seconds
            const increment = target / (duration / 16); // 60fps
            let current = 0;
            
            // Add animated class
            setTimeout(() => {
                counter.classList.add('ct-animated');
            }, index * 100);
            
            const updateCounter = () => {
                if (current < target) {
                    current += increment;
                    counter.textContent = Math.min(Math.ceil(current), target);
                    requestAnimationFrame(updateCounter);
                } else {
                    counter.textContent = target;
                }
            };
            
            // Start animation with delay
            setTimeout(() => {
                updateCounter();
            }, index * 100);
        });
    };
    
    // Intersection Observer for triggering animations
    if (typeof IntersectionObserver !== 'undefined') {
        const observerOptions = {
            threshold: 0.4,
            rootMargin: '0px 0px -100px 0px'
        };
        
        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting && !hasAnimated) {
                    animateCounters();
                }
            });
        }, observerOptions);
        
        const statsSection = document.querySelector('.ct-stats-section');
        if (statsSection) {
            observer.observe(statsSection);
        }
    }
    
    // Stat card hover effects
    const statCards = document.querySelectorAll('.ct-stat-card');
    
    statCards.forEach(card => {
        card.addEventListener('mouseenter', function() {
            // Add ripple effect
            const icon = this.querySelector('.ct-stat-icon');
            if (icon) {
                icon.style.transform = 'scale(1.1) rotate(5deg)';
            }
            
            // Highlight related stats
            const statNumber = this.querySelector('.ct-stat-number');
            if (statNumber) {
                statNumber.style.transform = 'scale(1.05)';
            }
        });
        
        card.addEventListener('mouseleave', function() {
            const icon = this.querySelector('.ct-stat-icon');
            if (icon) {
                icon.style.transform = '';
            }
            
            const statNumber = this.querySelector('.ct-stat-number');
            if (statNumber) {
                statNumber.style.transform = '';
            }
        });
    });
    
    // Intersection Observer for animations
    if (typeof IntersectionObserver !== 'undefined') {
        const observerOptions = {
            threshold: 0.2,
            rootMargin: '0px 0px -50px 0px'
        };
        
        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    entry.target.style.opacity = '1';
                    entry.target.style.transform = 'translateY(0)';
                }
            });
        }, observerOptions);
        
        // Observe stat items
        const statItems = document.querySelectorAll('.ct-stat-item');
        statItems.forEach(item => {
            item.style.opacity = '0';
            item.style.transform = 'translateY(30px)';
            item.style.transition = 'all 0.6s ease';
            observer.observe(item);
        });
    }
});
</script>