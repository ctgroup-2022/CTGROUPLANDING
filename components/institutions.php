<script>
document.addEventListener('DOMContentLoaded', function() {
    // Apply button functionality
    const applyBtn = document.getElementById('ctInstitutionsApplyBtn');
    const existingModal = document.getElementById('ctEducationalCampusModal');
    
    if (applyBtn && existingModal) {
        applyBtn.addEventListener('click', function(e) {
            e.preventDefault();
            existingModal.classList.add('ct-modal-active');
            document.body.style.overflow = 'hidden';
        });
    }
    
    // Institution card hover effects
    const institutionCards = document.querySelectorAll('.ct-institution-card');
    
    institutionCards.forEach(card => {
        card.addEventListener('mouseenter', function() {
            // Add subtle animation to highlights
            const highlights = this.querySelectorAll('.ct-highlight-item');
            highlights.forEach((highlight, index) => {
                setTimeout(() => {
                    highlight.style.transform = 'translateX(5px)';
                }, index * 50);
            });
        });
        
        card.addEventListener('mouseleave', function() {
            const highlights = this.querySelectorAll('.ct-highlight-item');
            highlights.forEach(highlight => {
                highlight.style.transform = 'translateX(0)';
            });
        });
    });
    
    // Intersection Observer for animations
    if (typeof IntersectionObserver !== 'undefined') {
        const observerOptions = {
            threshold: 0.2,
            rootMargin: '0px 0px -50px 0px'
        };
        
        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    entry.target.style.opacity = '1';
                    entry.target.style.transform = 'translateY(0)';
                }
            });
        }, observerOptions);
        
        // Observe all institution items
        const institutionItems = document.querySelectorAll('.ct-institution-item');
        institutionItems.forEach(item => {
            item.style.opacity = '0';
            item.style.transform = 'translateY(30px)';
            item.style.transition = 'all 0.6s ease';
            observer.observe(item);
        });
    }
    
    // Button click tracking (for analytics)
    const allInstitutionBtns = document.querySelectorAll('.ct-institution-btn');
    allInstitutionBtns.forEach(btn => {
        btn.addEventListener('click', function() {
            const institutionName = this.closest('.ct-institution-card')
                .querySelector('.ct-institution-name').textContent;
            const buttonType = this.classList.contains('ct-btn-primary') ? 'explore' : 'apply';
            
            // Track button click (you can integrate with your analytics)
            console.log(`Institution button clicked: ${institutionName} - ${buttonType}`);
        });
    });
});
</script>