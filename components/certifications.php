document.addEventListener('DOMContentLoaded', function() {
    // Apply button functionality
    const applyBtn = document.getElementById('ctCertificationsApplyBtn');
    const existingModal = document.getElementById('ctEducationalCampusModal');
    
    if (applyBtn && existingModal) {
        applyBtn.addEventListener('click', function(e) {
            e.preventDefault();
            existingModal.classList.add('ct-modal-active');
            document.body.style.overflow = 'hidden';
        });
    }
    
    // Intersection Observer for animations
    if (typeof IntersectionObserver !== 'undefined') {
        const observerOptions = {
            threshold: 0.2,
            rootMargin: '0px 0px -50px 0px'
        };
        
        const observer = new IntersectionObserver((entries) => {
            entries.forEach((entry, index) => {
                if (entry.isIntersecting) {
                    setTimeout(() => {
                        entry.target.classList.add('ct-animate');
                    }, index * 100);
                }
            });
        }, observerOptions);
        
        const bentoItems = document.querySelectorAll('.ct-bento-item');
        bentoItems.forEach(item => {
            observer.observe(item);
        });
    }
    
    // Grid lines animation trigger
    if (typeof IntersectionObserver !== 'undefined') {
        const gridObserver = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    const gridLines = entry.target.querySelectorAll('.ct-grid-line');
                    const gridDots = entry.target.querySelectorAll('.ct-grid-dot');
                    
                    gridLines.forEach((line, index) => {
                        setTimeout(() => {
                            line.style.opacity = '1';
                            line.style.transform = 'scale(1)';
                        }, index * 100);
                    });
                    
                    gridDots.forEach((dot, index) => {
                        setTimeout(() => {
                            dot.style.animation = 'ctGridDotPulse 3s ease infinite';
                        }, (index * 150) + 1000);
                    });
                }
            });
        }, { threshold: 0.3 });
        
        const gridOverlay = document.querySelector('.ct-grid-overlay');
        if (gridOverlay) {
            gridObserver.observe(gridOverlay);
        }
    }
    
    // Add parallax effect to background elements
    const bgElements = document.querySelectorAll('.ct-certifications-bg-element');
    
    window.addEventListener('scroll', function() {
        const scrolled = window.pageYOffset;
        const rate = scrolled * -0.5;
        
        bgElements.forEach((element, index) => {
            const multiplier = (index + 1) * 0.3;
            element.style.transform = `translateY(${rate * multiplier}px)`;
        });
    });
    
    // Enhanced hover effects for bento cards
    const bentoCards = document.querySelectorAll('.ct-bento-card');
    
    bentoCards.forEach(card => {
        card.addEventListener('mouseenter', function() {
            // Add subtle 3D effect
            this.style.transform = 'translateY(-8px) rotateX(5deg) rotateY(2deg)';
            this.style.transformStyle = 'preserve-3d';
            
            // Pulse nearby grid dots
            const gridDots = document.querySelectorAll('.ct-grid-dot');
            gridDots.forEach(dot => {
                dot.style.animationDuration = '1s';
            });
        });
        
        card.addEventListener('mouseleave', function() {
            this.style.transform = '';
            this.style.transformStyle = '';
            
            // Reset grid dots
            const gridDots = document.querySelectorAll('.ct-grid-dot');
            gridDots.forEach(dot => {
                dot.style.animationDuration = '3s';
            });
        });
        
        // Add click tracking
        card.addEventListener('click', function() {
            const title = this.querySelector('.ct-bento-title')?.textContent;
            console.log(`Certification clicked: ${title}`);
        });
    });
    
    // Keyboard navigation support
    bentoCards.forEach((card, index) => {
        card.setAttribute('tabindex', '0');
        card.addEventListener('keydown', function(e) {
            if (e.key === 'Enter' || e.key === ' ') {
                e.preventDefault();
                this.click();
            }
            
            // Arrow key navigation
            if (e.key === 'ArrowRight' && index < bentoCards.length - 1) {
                bentoCards[index + 1].focus();
            } else if (e.key === 'ArrowLeft' && index > 0) {
                bentoCards[index - 1].focus();
            } else if (e.key === 'ArrowDown' && index + 4 < bentoCards.length) {
                bentoCards[index + 4].focus();
            } else if (e.key === 'ArrowUp' && index - 4 >= 0) {
                bentoCards[index - 4].focus();
            }
        });
        
        card.addEventListener('focus', function() {
            this.style.outline = '2px solid var(--ct-primary)';
            this.style.outlineOffset = '4px';
        });
        
        card.addEventListener('blur', function() {
            this.style.outline = '';
            this.style.outlineOffset = '';
        });
    });
});
</script>