<script>
document.addEventListener('DOMContentLoaded', function() {
    // Campus comparison functionality
    const compareBtn = document.getElementById('ctCompareCampusesBtn');
    const existingModal = document.getElementById('ctEducationalCampusModal');
    
    if (compareBtn && existingModal) {
        compareBtn.addEventListener('click', function() {
            existingModal.classList.add('ct-modal-active');
            document.body.style.overflow = 'hidden';
        });
    }
    
    // Virtual tour functionality
    const tourBtns = document.querySelectorAll('.ct-tour-btn');
    tourBtns.forEach(btn => {
        btn.addEventListener('click', function() {
            const campus = this.getAttribute('data-campus');
            
            // You can implement virtual tour modal or redirect to virtual tour page
            const tourUrls = {
                shahpur: 'https://shahpur.ctgroup.in/virtual-tour',
                maqsudan: 'https://maqsudan.ctgroup.in/virtual-tour'
            };
            
            if (tourUrls[campus]) {
                window.open(tourUrls[campus], '_blank');
            }
            
            // Track virtual tour click
            console.log(`Virtual tour clicked: ${campus}`);
        });
    });
    
    // Campus card hover effects
    const campusCards = document.querySelectorAll('.ct-campus-card');
    
    campusCards.forEach(card => {
        card.addEventListener('mouseenter', function() {
            // Animate feature items
            const features = this.querySelectorAll('.ct-feature-item');
            features.forEach((feature, index) => {
                setTimeout(() => {
                    feature.style.transform = 'translateX(5px)';
                }, index * 50);
            });
        });
        
        card.addEventListener('mouseleave', function() {
            const features = this.querySelectorAll('.ct-feature-item');
            features.forEach(feature => {
                feature.style.transform = 'translateX(0)';
            });
        });
    });
    
    // Intersection Observer for animations
    if (typeof IntersectionObserver !== 'undefined') {
        const observerOptions = {
            threshold: 0.2,
            rootMargin: '0px 0px -50px 0px'
        };
        
        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    entry.target.style.opacity = '1';
                    entry.target.style.transform = 'translateY(0)';
                }
            });
        }, observerOptions);
        
        // Observe campus items
        const campusItems = document.querySelectorAll('.ct-campus-item');
        campusItems.forEach(item => {
            item.style.opacity = '0';
            item.style.transform = 'translateY(30px)';
            item.style.transition = 'all 0.6s ease';
            observer.observe(item);
        });
    }
    
    // Button click tracking
    const allCampusBtns = document.querySelectorAll('.ct-campus-btn');
    allCampusBtns.forEach(btn => {
        btn.addEventListener('click', function() {
            const campusName = this.closest('.ct-campus-card')
                .querySelector('.ct-campus-name').textContent;
            const buttonType = this.classList.contains('ct-btn-primary') ? 'apply' : 'explore';
            
            // Track button click for analytics
            console.log(`Campus button clicked: ${campusName} - ${buttonType}`);
        });
    });
    
    // Stats animation on scroll
    const animateStats = () => {
        const statNumbers = document.querySelectorAll('.ct-stat-number');
        
        statNumbers.forEach(stat => {
            const text = stat.textContent;
            const isNumeric = !isNaN(parseInt(text));
            
            if (isNumeric) {
                const target = parseInt(text);
                const increment = target / 50;
                let current = 0;
                
                const updateStat = () => {
                    if (current < target) {
                        current += increment;
                        stat.textContent = Math.ceil(current) + (text.includes('+') ? '+' : '') + (text.includes('K') ? 'K' : '');
                        setTimeout(updateStat, 30);
                    } else {
                        stat.textContent = text;
                    }
                };
                
                updateStat();
            }
        });
    };
    
    // Trigger stats animation when section comes into view
    const statsObserver = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                animateStats();
                statsObserver.unobserve(entry.target);
            }
        });
    }, { threshold: 0.5 });
    
    const campusSection = document.querySelector('.ct-campuses-section');
    if (campusSection) {
        statsObserver.observe(campusSection);
    }
});
</script>